char *pg_parser_errno_getErrInfo(int32_t pg_parser_errno)
{
    int32_t index = 0;
    char *errmsg_result = NULL;

    pg_parser_errno = (pg_parser_errno & ERRNO_INDEX);     /* make vs studio happy */
    if (0 == (pg_parser_errno & ERRNO_MASK))
    {
        /* 返回系统错误信息 */
        return strerror(errno);
    }
    for (index = 0; index < ERRNO_CNT; index++)
    {
        if (pg_parser_errno == m_errno_2_msg[index].m_errno)
        {
            errmsg_result = m_errno_2_msg[index].m_errormsg;
            break;
        }
    }
    if (NULL == errmsg_result)
        return "unknown errno code";

    return errmsg_result;
}
